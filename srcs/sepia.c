#include "rt.h"

void			sepia(t_rgb *v)
{
	t_rgb		n;

	n.r = (v->r * 0.393) + (v->g * 0.769) + (v->b * 0.189);
	n.g = (v->r * 0.349) + (v->g * 0.686) + (v->b * 0.168);
	n.b = (v->r * 0.272) + (v->g * 0.534) + (v->b * 0.131);
	if (n.r > 255)
		n.r = 255;
	if (n.g > 255)
		n.g = 255;
	if (n.b > 255)
		n.b = 255;
	v->r = n.r;
	v->g = n.g;
	v->b = n.b;
}

void			applied_sepia(t_env *e)
{
	int			i;

	i = 0;
	while (((i % WIN_X) < WIN_X) && ((i / WIN_X) < WIN_Y))
	{
		sepia(e->rgb[i]);
		i++;
	}
}
